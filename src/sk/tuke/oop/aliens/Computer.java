package sk.tuke.oop.aliens;


public class Computer {

    public int add(int num1, int num2) {
        return (num1 + num2);
    }

    public double add(double num1, double num2) {
        return (num1 + num2);
    }

    public int sub(int num1, int num2) {
        return (num1 - num2);
    }

    public double sub(double num1, double num2) {
        return (num1 - num2);
    }
}